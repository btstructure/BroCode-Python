#to do this import math
import math

pi = 3.14
neg_pi = -3.14
x,y,z = 5,10,8
#rounds the value to the nearest whole integer

print(round(pi))

#rounds the value up to the nearest whole integer, in the math module

print(math.ceil(pi))

#rounds the value down to the nearest whole integer, in the math module

print(math.floor(pi))

#gives the absolute value 

print(abs(neg_pi))

#raises the value to a power, needs 2 arguments

print(pow(pi, 2))

#gives the square root of the value, in the math module

print(math.sqrt(pi))

#finds the max value by the given values

print(max(x,y,z))

#finds the minimum value by the given values

print(min(x,y,z))